package com.zz.po;

public class CategoryStyle {
    private Integer sid;
    private String categoryimage;
    private String categorycolor;
    private Integer cid;
    private String categorytwocolor;
    private String categorycolorbg;

    public String getCategorytwocolor() {
        return categorytwocolor;
    }

    public void setCategorytwocolor(String categorytwocolor) {
        this.categorytwocolor = categorytwocolor;
    }

    public String getCategorycolorbg() {
        return categorycolorbg;
    }

    public void setCategorycolorbg(String categorycolorbg) {
        this.categorycolorbg = categorycolorbg;
    }

    public Integer getSid() {
        return sid;
    }

    public void setSid(Integer sid) {
        this.sid = sid;
    }

    public String getCategoryimage() {
        return categoryimage;
    }

    public void setCategoryimage(String categoryimage) {
        this.categoryimage = categoryimage;
    }

    public String getCategorycolor() {
        return categorycolor;
    }

    public void setCategorycolor(String categorycolor) {
        this.categorycolor = categorycolor;
    }

    public Integer getCid() {
        return cid;
    }

    public void setCid(Integer cid) {
        this.cid = cid;
    }
}
